package com.javarush.task.pro.task04.task0413;

/* 
Рисуем треугольник
*/

public class Solution {
    public static void main(String[] args) {
        int summ =1;
        for (int j = summ; j <= 10; j++) {
            int str = 0;
            for (int i = str; i < j; i++) {
                System.out.print("8");
            }
            System.out.println();

        }
    }
}

